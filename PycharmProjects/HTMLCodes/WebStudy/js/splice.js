/*
*splice(start: int, deleteCount: int, items...: any)
*实现删除、插入、和替换
*删除：(删除第一项的位置，删除的项数)
*splice(0, 2)
*插入：向指定位置插入任意数量的项
*(起始位置， 0(要删除的项数)， 要插入的项)
*splice(2,0,4,6)
*替换：向指定位置插入任意数量的项，同时删除任意数量的项
*(起始位置， (要删除的项数)， 要插入的项)
*splice(2,1,4,6)
*返回值：返回一个数组
*数组中包含从原数组删除的项，如果没有删除返回一个空数组
*/
var arr = [1,3,5,7,9,11];
var removed = arr.slice(0, 2);//[5,7,9,11]
var removed2 = arr.splice(2, 0, 4, 6);//[5,7,4,6,9,11]
//removed[]
var removed3 = arr.splice(1, 1, 2, 4);//[5,2,4,4,6,9,11]
